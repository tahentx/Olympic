<?php

function add($a, $b) {
    return $a + $b;
}

function subtract($a, $b) {
    return $a - $b;
}

function multiply($a, $b) {
    return $a * $b;
}

function divide($a, $b) {
    return $a / $b;
}

echo add(10,12) . PHP_EOL;
echo subtract(10,12) . PHP_EOL;
echo multiply(10,12) . PHP_EOL;
echo divide(10,12) . PHP_EOL;

?>
